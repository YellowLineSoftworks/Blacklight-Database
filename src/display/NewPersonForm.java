    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        name = jTextField1.getText();
        occupation = jTextField2.getText();
        int gen = jComboBox1.getSelectedIndex();
        if (gen == 1) {
            gender = false;
        } else {
            gender = true;
        }
        workplaceorschool = jTextField4.getText();
        picturefilepath = jTextField10.getText();
        phonenumber = jTextField5.getText();
        email = jTextField6.getText();
        address1 = jTextField7.getText();
        address2 = jTextField8.getText();
        address3 = jTextField9.getText();
        finished = true;
        logic.Person per = new logic.Person();
        per.name = name;
        MainGUI.model.addElement(name);
        per.occupation = occupation;
        per.address1 = address1;
        per.address2 = address2;
        per.address3 = address3;
        per.email = email;
        per.gender = gender;
        per.phonenumber = phonenumber;
        if (new File(picturefilepath).exists()) {
            per.picture = new ImageIcon(picturefilepath); 
            per.pictureFile = new File(picturefilepath);
        }
        per.workplaceorschool = workplaceorschool;
        String a = MainGUI.choice1.getSelectedItem();
        int counter = 0;
        while (counter < Database.opendatabases.size()) {
            if (Database.opendatabases.get(counter).name.equals(a)) {
                Database.opendatabases.get(counter).people.add(per);
            }
            counter++;
        }
        setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextField10MouseClicked
        int returnVal = jFileChooser1.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = jFileChooser1.getSelectedFile();
            try {
                jTextField10.setText(file.getPath());
            } catch (Exception e) {}
        } else {}
    }//GEN-LAST:event_jTextField10MouseClicked

